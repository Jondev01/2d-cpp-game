Map::Map(){
    sky = TextureManager::LoadTexture("sky.png");
    grass = TextureManager::LoadTexture("grass.png");
    spike = TextureManager::LoadTexture("spike.png");
    LoadMap(lvl1);
    src.x=src.y=0;
    src.w=dest.w=32;
    src.h=dest.h=32;
    dest.x=dest.y=0;
}

void Map::LoadMap(int arr[25][25]){
    for(int i=0; i<25; i++){
        for(int j=0; j<25; j++)
            theMap[i][j] = arr[i][j];
    }
}

void Map:: DrawMap(){
    int type = 0;
    for(int i=0; i<20; i++){
        for(int j=0; j<25; j++){
            type = theMap[i][j];
            dest.x = j*32;
            dest.y = i*32;
            switch(type){
            case 0:
                TextureManager::Draw(sky, src, dest);
                break;
            case 1:
                TextureManager::Draw(grass, src, dest);
                break;
            case 2:
                TextureManager::Draw(spike, src, dest);
                break;
            }
        }
    }

}
